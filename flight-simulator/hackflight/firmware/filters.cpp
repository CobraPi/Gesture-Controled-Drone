// complementary filter
float complementaryFilter(float a, float b, float c) 
{
    return a * c + b * (1 - c);
}

// deadband filter
int32_t deadbandFilter(int32_t value, int32_t deadband)
{
    if (abs(value) < deadband) {
        value = 0;
    } else if (value > 0) {
        value -= deadband;
    } else if (value < 0) {
        value += deadband;
    }
    return value;
}

